    private void btXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btXoaActionPerformed
        // TODO add your handling code here:
        DefaultTableModel modelcb = (DefaultTableModel) tb.getModel();

        int i = tb.getSelectedRow();
        if(i<0)
        {
            JOptionPane.showMessageDialog(null,"Bạn cần chọn dòng để xóa");
        }
        else{
            int chon = JOptionPane.showConfirmDialog(null,"Bạn có muốn xóa không","Xóa sách",JOptionPane.YES_NO_OPTION);
            if(chon == JOptionPane.YES_OPTION){
                NhanVienDTO cb = new NhanVienDTO();
                cb = NhanVienBUS.dsnv.remove(i);
                NhanVienBUS bus = new NhanVienBUS();
                if (i >= 0) {
                    xoaCB = tb.getValueAt(i, 0).toString();
                }
                bus.xoa(xoaCB);
                model.removeRow(i);
                tb.setModel(model);
            }
        }
    }//GEN-LAST:event_btXoaActionPerformed

