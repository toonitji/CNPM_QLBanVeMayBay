    private void btSuaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btSuaMouseClicked
        // TODO add your handling code here:

        DefaultTableModel modelcb = (DefaultTableModel) tb.getModel();
        int i = tb.getSelectedRow();
        String Manv = txtMaNv.getText();
        int check = 0;
        for (NhanVienDTO x : NhanVienBUS.dsnv) {
            if (x.getMaNv().equals(txtMaNv.getText())) {
                check = 1;
            }
        }
        NhanVienBUS.dsnv.stream().filter((x) -> (x.getMaNv().equals(Manv)));
        if (i < 0) {
            JOptionPane.showMessageDialog(null, "Bạn cần chọn dòng để sửa");
        } else if (check == 0) {
            JOptionPane.showMessageDialog(null, "Mã Nhân viên không được sửa");
            txtMaNv.requestFocus();

        } else {
            int chon = JOptionPane.showConfirmDialog(null, "Bạn có muốn sửa không", "Sửa", JOptionPane.YES_NO_OPTION);
            if (chon == JOptionPane.YES_OPTION && check == 1) {

                NhanVienDTO nv = new NhanVienDTO();
                nv.maNv = txtMaNv.getText();
                nv.ho = txtHo.getText();
                nv.ten = txtTen.getText();
                nv.diaChi = txtDiaChi.getText();
                nv.eMail = txtEmail.getText();
                nv.sDT = txtSDT.getText();
                nv.luong =txtLuong.getText();
                nv.soTK = txtSTK.getText();
                nv.maQuyen = txtMaQ.getText();
                NhanVienBUS bus = new NhanVienBUS();
                bus.sua(nv);

                tb.setModel(model);
                model.setValueAt(nv.maNv, i, 0);
                model.setValueAt(nv.ho, i, 1);
                model.setValueAt(nv.ten, i, 2);
                model.setValueAt(nv.diaChi, i, 3);
                model.setValueAt(nv.eMail, i, 4);
                model.setValueAt(nv.sDT, i, 5);
                model.setValueAt(nv.luong, i, 6);
                model.setValueAt(nv.soTK, i, 7);
                model.setValueAt(nv.maQuyen, i, 8);

                bus.docDSNV();
            }
        }
    }//GEN-LAST:event_btSuaMouseClicked

