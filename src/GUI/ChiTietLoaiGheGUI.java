    private void formWindowActivated(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowActivated
        // TODO add your handling code here:
        ChiTietLoaiGheBUS bus = new ChiTietLoaiGheBUS();
        if (ChiTietLoaiGheBUS.dsTable == null) {
            bus.docDSTable();
        }
        Vector header = new Vector();
        header.add("Mã Loại Ghế");
        header.add("Mã Chuyến");
        header.add("Tên Loại");
        header.add("Giá Vé");
        model = new DefaultTableModel(header, 0);
        for (ChiTietLoaiGheDTO tb : ChiTietLoaiGheBUS.dsTable) {
            Vector row = new Vector();
            row.add(tb.MaLoaiGhe);
            row.add(tb.MaChuyen);
            row.add(tb.TenLoai);
            row.add(tb.GiaVe);
            model.addRow(row);
        }
        tblDSLoaiGhe.setModel(model);
    }//GEN-LAST:event_formWindowActivated

    private void tblDSLoaiGheMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblDSLoaiGheMouseClicked
        // TODO add your handling code here:
        DefaultTableModel modelcb = (DefaultTableModel) tblDSLoaiGhe.getModel();
        int i = tblDSLoaiGhe.getSelectedRow();
        if (i >= 0) {
            xoaChiTietLoaiGhe = tblDSLoaiGhe.getValueAt(i, 0).toString();
            ChiTietLoaiGheDTO ctlg = new ChiTietLoaiGheDTO();
            ctlg = ChiTietLoaiGheBUS.dsTable.get(i);
            txtMaLoaiGhe.setText(ctlg.getMaLoaiGhe());
            txtMaChuyen.setText(ctlg.getMaChuyen());
            txtTenLoai.setText(ctlg.getTenLoai());
            txtGiaVe.setText(String.valueOf(ctlg.getGiaVe()));
            
        }
    }//GEN-LAST:event_tblDSLoaiGheMouseClicked

    private void btAddLoaiGheActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btAddLoaiGheActionPerformed
        // TODO add your handling code here:
        ChiTietLoaiGheDTO ctlg = new ChiTietLoaiGheDTO();
        ctlg.MaLoaiGhe = txtMaLoaiGhe.getText();
        ctlg.MaChuyen = txtMaChuyen.getText();
        ctlg.TenLoai = txtTenLoai.getText();
        ctlg.GiaVe = Integer.parseInt(txtGiaVe.getText());
        ChiTietLoaiGheBUS bus = new ChiTietLoaiGheBUS();
        bus.them(ctlg);
        Vector row = new Vector();
        row.add(ctlg.MaLoaiGhe);
        row.add(ctlg.MaChuyen);
        row.add(ctlg.TenLoai);
        row.add(ctlg.GiaVe);
        model.addRow(row);
        tblDSLoaiGhe.setModel(model);
    }//GEN-LAST:event_btAddLoaiGheActionPerformed

    private void btEditLoaiGheActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btEditLoaiGheActionPerformed
        // TODO add your handling code here:
        DefaultTableModel modelcb = (DefaultTableModel) tblDSLoaiGhe.getModel();
        int i = tblDSLoaiGhe.getSelectedRow();
        if (i < 0) {
            JOptionPane.showMessageDialog(null, "Bạn cần chọn dòng để sửa");
        }
        ChiTietLoaiGheDTO ctlg = new ChiTietLoaiGheDTO();
        ctlg.MaLoaiGhe = txtMaLoaiGhe.getText();
        ctlg.MaChuyen = txtMaChuyen.getText();
        ctlg.TenLoai = txtTenLoai.getText();
        ctlg.GiaVe =  Integer.parseInt(txtGiaVe.getText());
        ChiTietLoaiGheBUS bus = new ChiTietLoaiGheBUS();
        bus.sua(ctlg);
        
        tblDSLoaiGhe.setModel(model);
        model.setValueAt(ctlg.MaLoaiGhe, i, 0);
        model.setValueAt(ctlg.MaChuyen, i, 1);
        model.setValueAt(ctlg.TenLoai, i, 2);
        model.setValueAt(ctlg.GiaVe, i, 3);
        bus.docDSTable();
    }//GEN-LAST:event_btEditLoaiGheActionPerformed

    private void btDeleteLoaiGheActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btDeleteLoaiGheActionPerformed
        // TODO add your handling code here:
        DefaultTableModel modelcb = (DefaultTableModel) tblDSLoaiGhe.getModel();

        int i = tblDSLoaiGhe.getSelectedRow();
                if(i<0)
                 {
                     JOptionPane.showMessageDialog(null,"Bạn cần chọn dòng để xóa");
                 }
                else{
                      int chon = JOptionPane.showConfirmDialog(null,"Bạn có muốn xóa không","Xóa sách",JOptionPane.YES_NO_OPTION);
                      if(chon == JOptionPane.YES_OPTION){
                            ChiTietLoaiGheDTO ctlg = new ChiTietLoaiGheDTO();
                            ctlg = ChiTietLoaiGheBUS.dsTable.remove(i);
                            ChiTietLoaiGheBUS bus = new ChiTietLoaiGheBUS();
                            if (i >= 0) {
                            xoaChiTietLoaiGhe = tblDSLoaiGhe.getValueAt(i, 0).toString();
                            }
                            bus.xoa(xoaChiTietLoaiGhe);
                            model.removeRow(i);
                            tblDSLoaiGhe.setModel(model);
                      }
                }
    }//GEN-LAST:event_btDeleteLoaiGheActionPerformed

