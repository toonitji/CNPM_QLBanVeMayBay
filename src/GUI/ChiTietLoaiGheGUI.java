    private void btChonLoaiGheActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btChonLoaiGheActionPerformed
        // TODO add your handling code here:
        int i = tblDSLoaiGhe.getSelectedRow();
            if(i>=0)
            {
                ChiTietLoaiGheDTO ctlg = new ChiTietLoaiGheDTO();
                ChiTietLoaiGheBUS bus = new ChiTietLoaiGheBUS();
                if(ChiTietLoaiGheBUS.dsTable == null) bus.docDSTable();
                ctlg = bus.dsTable.get(i);
                maloaighe = ctlg.getMaLoaiGhe();
                VeMayBayGUI.txtMaLoaiGhe.setText(maloaighe);
                VeMayBayGUI.txtMaLoaiGhe.setEditable(false);
                this.dispose();
            }
            else
            {
                JOptionPane.showMessageDialog(null, "Vui lòng chọn dòng trên bảng");
            }
    }//GEN-LAST:event_btChonLoaiGheActionPerformed

