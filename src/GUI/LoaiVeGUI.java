    private void btAddLoaiVeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btAddLoaiVeActionPerformed
        // TODO add your handling code here:
        LoaiVeDTO lv = new LoaiVeDTO();
        lv.MaLoaiVe = txtMaLoaiVe.getText();
        lv.TenLoai = txtTenLoai.getText();
        lv.GiaTien = Integer.parseInt(txtGiaTien.getText());
        LoaiVeBUS bus = new LoaiVeBUS();
        bus.them(lv);
        Vector row = new Vector();
        row.add(lv.MaLoaiVe);
        row.add(lv.TenLoai);
        row.add(lv.GiaTien);
        model.addRow(row);
        tblDSLV.setModel(model);
    }//GEN-LAST:event_btAddLoaiVeActionPerformed

    private void btInsertLoaiVeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btInsertLoaiVeActionPerformed
        // TODO add your handling code here:
        DefaultTableModel modelcb = (DefaultTableModel) tblDSLV.getModel();
        int i = tblDSLV.getSelectedRow();
        if (i < 0) {
            JOptionPane.showMessageDialog(null, "Bạn cần chọn dòng để sửa");
        }
        LoaiVeDTO lv = new LoaiVeDTO();
        lv.MaLoaiVe = txtMaLoaiVe.getText();
        lv.TenLoai = txtTenLoai.getText();
        lv.GiaTien =  Integer.parseInt(txtGiaTien.getText());
        LoaiVeBUS bus = new LoaiVeBUS();
        bus.sua(lv);
        
        tblDSLV.setModel(model);
        model.setValueAt(lv.MaLoaiVe, i, 0);
        model.setValueAt(lv.TenLoai, i, 1);
        model.setValueAt(lv.GiaTien, i, 2);
        bus.docDSLoaiVe();
    }//GEN-LAST:event_btInsertLoaiVeActionPerformed

    private void btDeleteLoaiVeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btDeleteLoaiVeActionPerformed
        // TODO add your handling code here:
         DefaultTableModel modelcb = (DefaultTableModel) tblDSLV.getModel();

        int i = tblDSLV.getSelectedRow();
                if(i<0)
                 {
                     JOptionPane.showMessageDialog(null,"Bạn cần chọn dòng để xóa");
                 }
                else{
                      int chon = JOptionPane.showConfirmDialog(null,"Bạn có muốn xóa không","Xóa sách",JOptionPane.YES_NO_OPTION);
                      if(chon == JOptionPane.YES_OPTION){
                            LoaiVeDTO lv = new LoaiVeDTO();
                            lv = LoaiVeBUS.dsLoaiVe.remove(i);
                            LoaiVeBUS bus = new LoaiVeBUS();
                            if (i >= 0) {
                            xoaloaive = tblDSLV.getValueAt(i, 0).toString();
                            }
                            bus.xoa(xoaloaive);
                            model.removeRow(i);
                            tblDSLV.setModel(model);
                      }
                }
    }//GEN-LAST:event_btDeleteLoaiVeActionPerformed

    private void btChonLoaiVeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btChonLoaiVeActionPerformed
        // TODO add your handling code here:
        int i = tblDSLV.getSelectedRow();
            if(i>=0)
            {
                LoaiVeDTO lv = new LoaiVeDTO();
                LoaiVeBUS bus = new LoaiVeBUS();
                if(LoaiVeBUS.dsLoaiVe == null) bus.docDSLoaiVe();
                lv = bus.dsLoaiVe.get(i);
                maloaive = lv.getMaLoaiVe();
                VeMayBayGUI.txtMaLoaiVe.setText(maloaive);
                VeMayBayGUI.txtMaLoaiVe.setEditable(false);
                this.dispose();
            }
            else
            {
                JOptionPane.showMessageDialog(null, "Vui lòng chọn dòng trên bảng");
            }
    }//GEN-LAST:event_btChonLoaiVeActionPerformed

