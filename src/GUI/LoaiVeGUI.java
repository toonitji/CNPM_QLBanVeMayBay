    private void tblDSLVMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblDSLVMouseClicked
        // TODO add your handling code here:
       DefaultTableModel modelcb = (DefaultTableModel) tblDSLV.getModel();
        int i = tblDSLV.getSelectedRow();
        if (i >= 0) {
            xoaloaive = tblDSLV.getValueAt(i, 0).toString();
            LoaiVeDTO loaive = new LoaiVeDTO();
            loaive = LoaiVeBUS.dsLoaiVe.get(i);
            txtMaLoaiVe.setText(loaive.getMaLoaiVe());
            txtTenLoai.setText(loaive.getTenLoai());
            txtGiaTien.setText(String.valueOf(loaive.getGiaTien()));
            
        }
    }//GEN-LAST:event_tblDSLVMouseClicked

